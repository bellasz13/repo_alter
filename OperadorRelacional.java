package operadorelac;

public class OperadorRelacional {

	public static void main(String args[]){
		
		System.out.println(5 > 2); //true
		System.out.println(5 >= 2); //true
		System.out.println(5 < 2); //false
		System.out.println(5 <= 2); //false
		System.out.println(5 != 2); //true
		}
}
