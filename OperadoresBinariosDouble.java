package operadoresbidouble;

public class OperadoresBinariosDouble {

	public static void main(String args[]){
		int num1 = 3, num2 = 5;

		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);
		System.out.println("(double) num1 / num2 = " + ((double) num1 / num2));
		System.out.println("num1 / (double) num2 = " + (num1 / (double) num2));
		System.out.println("(double) num1 / (double) num2 = " + ((double) num1 /
		(double) num2));
		}
}
