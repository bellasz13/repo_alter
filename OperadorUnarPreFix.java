package operadoresunarprefix;

public class OperadorUnarPreFix {

	public static void main(String[] args) {
		int num = 5;
		System.out.println(num); //5
		System.out.println(++num); //6
		System.out.println(num); //6
		System.out.println(--num); //5
		System.out.println(num); //5
		}
}
